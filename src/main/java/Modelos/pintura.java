package Modelos;

/**
 *
 * @author conej
 */
public class pintura {
    int idObraDeArte;
    String tipo_pintura;
    String material;
    String estilo;
    public pintura() { }

    public int getIdObraDeArte() {
        return idObraDeArte;
    }

    public void setIdObraDeArte(int idObraDeArte) {
        this.idObraDeArte = idObraDeArte;
    }

    public String getTipo_pintura() {
        return tipo_pintura;
    }

    public void setTipo_pintura(String tipo_pintura) {
        this.tipo_pintura = tipo_pintura;
    }

    public String getMaterial() {
        return material;
    }

    public void setMaterial(String material) {
        this.material = material;
    }

    public String getEstilo() {
        return estilo;
    }

    public void setEstilo(String estilo) {
        this.estilo = estilo;
    }
    
}
